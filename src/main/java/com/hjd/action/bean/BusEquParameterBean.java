package com.hjd.action.bean;

import com.hjd.base.BeanAbs;

public class BusEquParameterBean extends BeanAbs {
	
	//以下是用于添加设备小类和设备参数的关系的属性
	/**
	 * 设备小类的
	 */
	private Integer equNameId;
	/**
	 * 生产厂家的ID
	 */
	private Long manufactruerId;
	/**
	 * 品牌的ID
	 */
	private Long brandId;
	/**
	 * 型号的ID
	 */
	private Long modelId;
	/**
	 * 型号的名称
	 */
	private String modelName;
	/**
	 * 规格的ID
	 */
	private Long standardId;
	/**
	 * 规格的名称
	 */
	private String standardName;
	
	//以下是设备参数的属性，用于添加设备参数的添加
	/**
	 * 设备参数的ID
	 */
	private Long parameterId;
	
	/**
	 * 设备参数的类型，1：品牌，2：生产厂家，3：型号，4：规格
	 */
	private Integer type;
	
	/**
	 * 设备参数的名称
	 */
	private String name;
	
	/**
	 * 设备参数的状态，0：删除，1：正常
	 */
	private Integer status;
	
	/**
	 * 设备参数的备注
	 */
	private String note;
	
	/**
	 * 品牌的Logo图片
	 */
	private String logoPic;

	public Long getParameterId() {
		return parameterId;
	}

	public void setParameterId(Long parameterId) {
		this.parameterId = parameterId;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public String getLogoPic() {
		return logoPic;
	}

	public void setLogoPic(String logoPic) {
		this.logoPic = logoPic;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	public Integer getStatus() {
		return status;
	}

	public void setStatus(Integer status) {
		this.status = status;
	}

	public Integer getEquNameId() {
		return equNameId;
	}

	public void setEquNameId(Integer equNameId) {
		this.equNameId = equNameId;
	}

	public Long getManufactruerId() {
		return manufactruerId;
	}

	public void setManufactruerId(Long manufactruerId) {
		this.manufactruerId = manufactruerId;
	}

	public Long getBrandId() {
		return brandId;
	}

	public void setBrandId(Long brandId) {
		this.brandId = brandId;
	}

	public Long getModelId() {
		return modelId;
	}

	public void setModelId(Long modelId) {
		this.modelId = modelId;
	}

	public String getModelName() {
		return modelName;
	}

	public void setModelName(String modelName) {
		this.modelName = modelName;
	}

	public Long getStandardId() {
		return standardId;
	}

	public void setStandardId(Long standardId) {
		this.standardId = standardId;
	}

	public String getStandardName() {
		return standardName;
	}

	public void setStandardName(String standardName) {
		this.standardName = standardName;
	}

}
