package com.hjd.action.bean;

import java.sql.Date;

import com.hjd.base.IFPageRequest;

public class ATApplySearchBean extends IFPageRequest {

	/**
	 * 当前所属企业或部门标识
	 */
	private Long currOrgId;
	/**
	 * 申请开始日期（非必输）
	 */
	private Date beginDate;
	/**
	 * 申请结束日期（非必输）
	 */
	private Date endDate;
	/**
	 * 本人标识（个人查询时，必输）
	 */
	private Long partyId;
	/**
	 * 状态（非必输）
	 */
	private Integer state;
	/**
	 * 预订申请单标识（非必输）
	 */
	private Long dataId;
	/**
	 * 预订申请人姓名/出发城市/出发城市（非必输）
	 */
	private String fuzzyData;

	public Long getCurrOrgId() {
		return currOrgId;
	}
	public void setCurrOrgId(Long currOrgId) {
		this.currOrgId = currOrgId;
	}
	public Date getBeginDate() {
		return beginDate;
	}
	public void setBeginDate(Date beginDate) {
		this.beginDate = beginDate;
	}
	public Date getEndDate() {
		return endDate;
	}
	public void setEndDate(Date endDate) {
		this.endDate = endDate;
	}
	public Long getPartyId() {
		return partyId;
	}
	public void setPartyId(Long partyId) {
		this.partyId = partyId;
	}
	public Integer getState() {
		return state;
	}
	public void setState(Integer state) {
		this.state = state;
	}
	public Long getDataId() {
		return dataId;
	}
	public void setDataId(Long dataId) {
		this.dataId = dataId;
	}
	public String getFuzzyData() {
		return fuzzyData;
	}
	public void setFuzzyData(String fuzzyData) {
		this.fuzzyData = fuzzyData;
	}

	}
